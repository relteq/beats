    /**
     * Retrieve a multiple objects by pk
     *
     * @param pks List of primary keys
     * @param dbcon the connection to use
     * @throws TorqueException Any exceptions caught during processing will be
     *         rethrown wrapped into a TorqueException.
     */
    public static List retrieveByPKs( List pks, Connection dbcon )
        throws TorqueException
    {
        List objs = null;
        if (pks == null || pks.size() == 0)
        {
            objs = new LinkedList();
        }
        else
        {
            Criteria criteria = new Criteria();
            Iterator iter = pks.iterator();
            while (iter.hasNext())
            {
                ObjectKey pk = (ObjectKey) iter.next();
                SimpleKey[] keys = (SimpleKey[])pk.getValue();
                    Criteria.Criterion c0 = criteria.getNewCriterion(
                        NETWORK_ID, keys[0], Criteria.EQUAL);
                    Criteria.Criterion c1 = criteria.getNewCriterion(
                        LINK_ID, keys[1], Criteria.EQUAL);
                        c0.and(c1);
                    Criteria.Criterion c2 = criteria.getNewCriterion(
                        DATA_SOURCE_ID, keys[2], Criteria.EQUAL);
                        c1.and(c2);
                    Criteria.Criterion c3 = criteria.getNewCriterion(
                        TS, keys[3], Criteria.EQUAL);
                        c2.and(c3);
                    Criteria.Criterion c4 = criteria.getNewCriterion(
                        AGGREGATION, keys[4], Criteria.EQUAL);
                        c3.and(c4);
                    Criteria.Criterion c5 = criteria.getNewCriterion(
                        TYPE, keys[5], Criteria.EQUAL);
                        c4.and(c5);
                    Criteria.Criterion c6 = criteria.getNewCriterion(
                        CELL_NUMBER, keys[6], Criteria.EQUAL);
                        c5.and(c6);
                criteria.or(c0);
            }
        objs = doSelect(criteria, dbcon);
        }
        return objs;
    }

 

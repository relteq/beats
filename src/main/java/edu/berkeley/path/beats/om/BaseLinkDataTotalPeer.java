    /**
     * selects a collection of LinkDataTotal objects pre-filled with their
     * Links objects.
     *
     * This method is protected by default in order to keep the public
     * api reasonable.  You can provide public methods for those you
     * actually need in LinkDataTotalPeer.
     *
     * @throws TorqueException Any exceptions caught during processing will be
     *         rethrown wrapped into a TorqueException.
     */
    protected static List doSelectJoinLinks(Criteria criteria, Connection conn)
        throws TorqueException
    {
        setDbName(criteria);

        LinkDataTotalPeer.addSelectColumns(criteria);
        int offset = numColumns + 1;
        LinksPeer.addSelectColumns(criteria);

        criteria.addJoin(LinkDataTotalPeer.LINK_ID,
            LinksPeer.ID);
        criteria.addJoin(LinkDataTotalPeer.NETWORK_ID,
            LinksPeer.NETWORK_ID);

        correctBooleans(criteria);

        List rows;
        if (conn == null)
        {
            rows = BasePeer.doSelect(criteria);
        }
        else
        {
            rows = BasePeer.doSelect(criteria,conn);
        }

        List results = new ArrayList();

        for (int i = 0; i < rows.size(); i++)
        {
            Record row = (Record) rows.get(i);

            Class omClass = LinkDataTotalPeer.getOMClass();
            LinkDataTotal obj1 = (LinkDataTotal) LinkDataTotalPeer
                .row2Object(row, 1, omClass);
             omClass = LinksPeer.getOMClass();
            Links obj2 = (Links) LinksPeer
                .row2Object(row, offset, omClass);

            boolean newObject = true;
            for (int j = 0; j < results.size(); j++)
            {
                LinkDataTotal temp_obj1 = (LinkDataTotal) results.get(j);
                Links temp_obj2 = (Links) temp_obj1.getLinks();
                if (temp_obj2.getPrimaryKey().equals(obj2.getPrimaryKey()))
                {
                    newObject = false;
                    temp_obj2.addLinkDataTotal(obj1);
                    break;
                }
            }
            if (newObject)
            {
                obj2.initLinkDataTotals();
                obj2.addLinkDataTotal(obj1);
            }
            results.add(obj1);
        }
        return results;
    }





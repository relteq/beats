    /**
     * selects a collection of FundamentalDiagrams objects pre-filled with their
     * FundamentalDiagramProfiles objects.
     *
     * This method is protected by default in order to keep the public
     * api reasonable.  You can provide public methods for those you
     * actually need in FundamentalDiagramsPeer.
     *
     * @throws TorqueException Any exceptions caught during processing will be
     *         rethrown wrapped into a TorqueException.
     */
    protected static List doSelectJoinFundamentalDiagramProfiles(Criteria criteria, Connection conn)
        throws TorqueException
    {
        setDbName(criteria);

        FundamentalDiagramsPeer.addSelectColumns(criteria);
        int offset = numColumns + 1;
        FundamentalDiagramProfilesPeer.addSelectColumns(criteria);

        criteria.addJoin(FundamentalDiagramsPeer.FUNDAMENTAL_DIAGRAM_PROFILE_ID,
            FundamentalDiagramProfilesPeer.ID);

        correctBooleans(criteria);

        List rows;
        if (conn == null)
        {
            rows = BasePeer.doSelect(criteria);
        }
        else
        {
            rows = BasePeer.doSelect(criteria,conn);
        }

        List results = new ArrayList();

        for (int i = 0; i < rows.size(); i++)
        {
            Record row = (Record) rows.get(i);

            Class omClass = FundamentalDiagramsPeer.getOMClass();
            FundamentalDiagrams obj1 = (FundamentalDiagrams) FundamentalDiagramsPeer
                .row2Object(row, 1, omClass);
             omClass = FundamentalDiagramProfilesPeer.getOMClass();
            FundamentalDiagramProfiles obj2 = (FundamentalDiagramProfiles) FundamentalDiagramProfilesPeer
                .row2Object(row, offset, omClass);

            boolean newObject = true;
            for (int j = 0; j < results.size(); j++)
            {
                FundamentalDiagrams temp_obj1 = (FundamentalDiagrams) results.get(j);
                FundamentalDiagramProfiles temp_obj2 = (FundamentalDiagramProfiles) temp_obj1.getFundamentalDiagramProfiles();
                if (temp_obj2.getPrimaryKey().equals(obj2.getPrimaryKey()))
                {
                    newObject = false;
                    temp_obj2.addFundamentalDiagrams(obj1);
                    break;
                }
            }
            if (newObject)
            {
                obj2.initFundamentalDiagramss();
                obj2.addFundamentalDiagrams(obj1);
            }
            results.add(obj1);
        }
        return results;
    }





    /**
     * Fills the copyObj with the contents of this object.
     * If deepcopy is true, The associated objects are also copied
     * and treated as new objects.
     *
     * @param copyObj the object to fill.
     * @param deepcopy whether the associated objects should be copied.
     */
    protected Tables copyInto(Tables copyObj, boolean deepcopy) throws TorqueException
    {
        copyObj.setId(id);
        copyObj.setParentElementId(parentElementId);
        copyObj.setParentElementType(parentElementType);
        copyObj.setName(name);

        copyObj.setId((Long)null);

        if (deepcopy)
        {


        List vTabularDatas = getTabularDatas();
        if (vTabularDatas != null)
        {
            for (int i = 0; i < vTabularDatas.size(); i++)
            {
                TabularData obj = (TabularData) vTabularDatas.get(i);
                copyObj.addTabularData(obj.copy());
            }
        }
        else
        {
            copyObj.collTabularDatas = null;
        }


        List vTabularDataKeyss = getTabularDataKeyss();
        if (vTabularDataKeyss != null)
        {
            for (int i = 0; i < vTabularDataKeyss.size(); i++)
            {
                TabularDataKeys obj = (TabularDataKeys) vTabularDataKeyss.get(i);
                copyObj.addTabularDataKeys(obj.copy());
            }
        }
        else
        {
            copyObj.collTabularDataKeyss = null;
        }
        }
        return copyObj;
    }
        
    
